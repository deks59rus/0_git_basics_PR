def add(a,b):
    """
    :param a:
    :param b:
    :return: сумма чисел
    """
    return a+b

def sub(a,b):
    """
    :param a:
    :param b:
    :return: разность чисел
    """
    return a-b
def mul(a,b):
    """
    :param a:
    :param b:
    :return: произведение чисел
    """
    return a*b
def div(a,b):
    """
    :param a:
    :param b:
    :return: частное чисел чисел
    """
    return a/b